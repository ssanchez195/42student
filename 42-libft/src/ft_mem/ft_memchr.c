#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t len)
{
	size_t			i;
	unsigned char	*str;
	unsigned char	to_find;

	i = 0;
	str = (unsigned char *)s;
	to_find = (unsigned char)c;
	while (i < len)
	{
		if (str[i] == to_find)
			return (&str[i]);
		i++;
	}
	return (0);
}
